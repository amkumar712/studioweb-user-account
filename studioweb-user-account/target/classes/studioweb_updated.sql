CREATE TABLE USER_ACCOUNT 
        (
                USER_ID VARCHAR(20) NOT NULL, 
                FIRST_NAME VARCHAR(30) NOT NULL, 
                LAST_NAME VARCHAR(30) NOT NULL, 
                EMAIL VARCHAR(60) UNIQUE NOT NULL, 
                DISPLAY_NAME VARCHAR(30), 
                GENDER VARCHAR(1) NOT NULL, 
                DOB DATE, 
                USER_TYPE_ID VARCHAR(1) NOT NULL, -- REFERED FROM USER_TYPE TABLE
                ACTIVE VARCHAR(1) NOT NULL, 
                LAST_UPDATED TIMESTAMP NOT NULL, 
                CREATED TIMESTAMP NOT NULL
        );
        
CREATE TABLE USER_TYPE
        (
                USER_TYPE_ID INT NOT NULL,
                USER_TYPE_DESC VARCHAR(20)
        );
        
CREATE TABLE USER_LOGIN 
        (ID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1), 
        USER_ID VARCHAR(20),
        PASSWORD_HASH VARCHAR(128),
        PASSWORD_SALT VARCHAR(128),
        CONSTRAINT USER_ID_FK FOREIGN KEY (USER_ID) REFERENCES USER_ACCOUNT(USER_ID));
        
ALTER TABLE USER_LOGIN
        ADD CONSTRAINT ID_PK PRIMARY KEY (ID);
        
ALTER TABLE USER_ACCOUNT 
        ADD CONSTRAINT USER_ID_PK PRIMARY KEY (USER_ID);
        
ALTER TABLE USER_TYPE
        ADD CONSTRAINT USER_TYPE_ID_PK PRIMARY KEY (USER_TYPE_ID);
        
        
